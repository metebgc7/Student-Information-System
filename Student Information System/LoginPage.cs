        }

        private void mouseMove_Event(object sender, MouseEventArgs e)
        {
            if(mouseDown==true) 
            {
                Point currentScreenPos = PointToScreen(e.Location);
                Location=new Point(currentScreenPos.X - offset.X, currentScreenPos.Y - offset.Y);
            }
        }

